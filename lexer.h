// lexer.h
#ifndef LEXER_H
#define LEXER_H

#include "token.h"
#include <string>
#include <vector>
#include <map>
#include <set>

//============================================================================
// Лексический анализатор (сканер)
// Реализует детерминированный конечный автомат с действиями
// Состояние автомата сохраняется между вызовами nextToken()
//============================================================================

class Lexer {
private:
    //-----------------------------------------------------------------------
    // Состояния конечного автомата (согласно диаграмме из учебника)
    //-----------------------------------------------------------------------
    enum class State {
        H,          // Начальное состояние (пропуск пробелов)
        IDENT,      // Распознавание идентификатора или служебного слова
        NUMB,       // Распознавание целого числа
        REAL,       // Распознавание вещественного числа (после точки)
        STRING,     // Распознавание строковой константы
        COM,        // Пропуск комментария /* ... */
        ALE,        // Распознавание составных операторов (:=, <=, >=, ==, !=)
        NEQ,        // Специальное состояние для != (после !)
        DELIM       // Одиночный разделитель
    };
    
    //-----------------------------------------------------------------------
    // Поля класса
    //-----------------------------------------------------------------------
    
    std::string source;     // исходный код программы
    size_t pos;             // текущая позиция в исходном коде
    int line;               // текущая строка (для сообщений об ошибках)
    int column;             // текущая колонка
    
    State state;            // ТЕКУЩЕЕ состояние автомата (сохраняется!)
    std::string buffer;     // буфер для накопления лексемы
    int tempInt;            // временное значение для числа (схема Горнера)
    double tempReal;        // временное значение для вещественного числа
    double realDivider;     // делитель для дробной части (0.1, 0.01, ...)
    
    //-----------------------------------------------------------------------
    // Таблицы для быстрого поиска (аналог TW, TD из учебника)
    //-----------------------------------------------------------------------
    
    // Множество символов, которые могут начинать идентификатор
    std::set<char> identifierStartChars;
    
    // Множество символов, которые могут быть в идентификаторе (после первого)
    std::set<char> identifierChars;
    
    // Карта: строка оператора -> тип токена
    std::map<std::string, TokenType> operatorMap;
    
    //-----------------------------------------------------------------------
    // Приватные методы
    //-----------------------------------------------------------------------
    
    // Работа с символами
    char peek() const;                      // посмотреть текущий символ
    char peekNext() const;                  // посмотреть следующий символ
    char advance();                         // взять текущий символ и перейти дальше
    bool isEOF() const { return pos >= source.length(); }
    
    // Пропуск пробелов и комментариев
    void skipWhitespace();
    void skipComment();
    
    // Обработка разных состояний автомата
    Token processH();           // начальное состояние
    Token processIDENT();       // идентификатор / служебное слово
    Token processNUMB();        // целое число
    Token processREAL();        // вещественное число
    Token processSTRING();      // строковая константа
    Token processCOM();         // комментарий
    Token processALE();         // составной оператор (:=, <=, >=, ==, !=)
    Token processNEQ();         // !=
    Token processDELIM();       // одиночный разделитель
    
    // Вспомогательные методы
    void resetBuffer();         // очистить буфер
    void addToBuffer(char c);   // добавить символ в буфер
    void addToBuffer();         // добавить текущий символ в буфер
    
    // Инициализация таблиц
    void initCharSets();
    void initOperatorMap();
    
public:
    //-----------------------------------------------------------------------
    // Конструкторы и деструктор
    //-----------------------------------------------------------------------
    
    explicit Lexer(const std::string& sourceCode);
    ~Lexer() = default;
    
    //-----------------------------------------------------------------------
    // Основной метод — получение следующего токена
    //-----------------------------------------------------------------------
    
    Token nextToken();
    
    //-----------------------------------------------------------------------
    // Методы для работы с текущим токеном (для удобства парсера)
    //-----------------------------------------------------------------------
    
    Token currentToken;                     // последний прочитанный токен
    void advanceToken() { currentToken = nextToken(); }
    bool is(TokenType t) const { return currentToken.type == t; }
    bool isOneOf(const std::vector<TokenType>& types) const;
    void expect(TokenType t, const std::string& errorMsg);
    
    //-----------------------------------------------------------------------
    // Информация об ошибках
    //-----------------------------------------------------------------------
    
    std::string getPosition() const;        // "строка:колонка"
    std::string getErrorLocation() const;   // подробное сообщение о позиции
};

#endif // LEXER_H
#pragma once
// ============================================================
// poliz.h — ПОЛИЗ (Польская Инверсная Запись)
//
// ПОЛИЗ — постфиксная форма записи программы.
// Программа = вектор инструкций PolizOp.
// Выполняется стековой виртуальной машиной (см. interpreter.h).
//
// ЗАЧЕМ ПОЛИЗ?
//   Удобнее чем выполнять программу "на лету" при разборе:
//   — можно выполнять программу много раз (разные входные данные)
//   — управление потоком (if/for/goto) реализуется через JMP/JZ/JNZ
//
// КАК РАБОТАЕТ СТЕК?
//   PUSH кладёт значение на стек.
//   Арифметика (ADD, MUL, ...) снимает операнды и кладёт результат.
//
//   Пример: a + b * 2
//     LOAD a    → стек: [a]
//     LOAD b    → стек: [a, b]
//     PUSH 2    → стек: [a, b, 2]
//     MUL       → стек: [a, b*2]
//     ADD       → стек: [a+b*2]
//
// КАК РАБОТАЕТ for I = E1 step E2 until E3 do S:
//   [E1] STORE I POP
//   [E2] STORE __step_N POP
//   [E3] STORE __limit_N POP
//  check:
//   LOAD I; LOAD __limit_N; GT; JNZ→end
//   [S]
//   LOAD I; LOAD __step_N; ADD; STORE I; POP
//   JMP→check
//  end:
//
// КАК РАБОТАЕТ goto (forward/backward):
//   Backward (метка уже известна): emit JMP с готовым адресом
//   Forward  (метка ещё впереди):  emit JMP 0, адрес заполним позже
//
// КАК РАБОТАЮТ ОБЫЧНЫЕ and/or (вариант VI.2):
//   Оба операнда ВСЕГДА вычисляются, потом инструкция AND/OR:
//     [левый] [правый] AND
//     [левый] [правый] OR
// ============================================================
#include <string>

enum class OpCode {
    // --- Положить значение на стек ---
    PUSH_INT,   // стек ← ival (long long)
    PUSH_REAL,  // стек ← rval (double)
    PUSH_STR,   // стек ← sval (string)

    // --- Работа с переменными ---
    LOAD,       // стек ← значение переменной sval
    STORE,      // переменная sval ← вершина стека (значение НЕ снимается:
                // результат a=expr равен expr, как в C)

    // --- Арифметика (снимают 2 операнда, кладут результат) ---
    ADD,        // сложение (или конкатенация строк)
    SUB,        // вычитание
    MUL,        // умножение
    DIV,        // деление (проверяем деление на 0)
    MOD,        // остаток от деления (только int)
    NEG,        // унарный минус (снимает 1 операнд) ← вариант V.1

    // --- Сравнения (снимают 2, кладут 0 или 1) ---
    LT,         // <
    GT,         // >
    LE,         // <=
    GE,         // >=
    EQ,         // ==
    NEQ,        // !=

    // --- Логика (ОБЫЧНЫЕ вычисления, вариант VI.2) ---
    // Оба операнда уже на стеке к моменту выполнения AND/OR
    AND,        // снять R,L → (L≠0 && R≠0) ? 1 : 0
    OR,         // снять R,L → (L≠0 || R≠0) ? 1 : 0
    NOT,        // снять v   → (v==0) ? 1 : 0

    // --- Переходы ---
    JMP,        // безусловный переход к инструкции с индексом ival
    JZ,         // снять со стека; если 0 → перейти к ival
                //   используется для: if, while
    JNZ,        // снять со стека; если НЕ 0 → перейти к ival
                //   используется для: for (выход когда I > limit)

    // --- Ввод / Вывод ---
    READ,       // прочитать с cin в переменную sval
    WRITE,      // снять со стека, вывести в cout

    // --- Служебные ---
    POP         // снять вершину стека (мусор от оператора-выражения)
};

// Одна инструкция ПОЛИЗ
struct PolizOp {
    OpCode      code;
    long long   ival = 0;    // операнд для PUSH_INT и адресов JMP/JZ/JNZ
    double      rval = 0.0;  // операнд для PUSH_REAL
    std::string sval;        // операнд для PUSH_STR, LOAD, STORE, READ
};
